import pkg from "pg";
import dotenv from "dotenv";
dotenv.config();

const { Pool } = pkg;

const pool = new Pool({
  connectionString: process.env.DATABASE_URL,
});

const connectDB = async () => {
  try {
    await pool.connect();
    console.log("✅ Postgres Connected");
  } catch (err) {
    console.log("❌ DB Connection Failed ", err);
    process.exit(1);
  }
};

export default connectDB;
export { pool };
